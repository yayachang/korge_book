fun main() {
    val integerArray = arrayOf(1, 2, 3)
    val doubleArray = arrayOf(1.2, 3.3, 4.5)

    println("integerArray:${integerArray[0]},${integerArray[1]},${integerArray[2]}")
    println("doubleArray:${doubleArray[0]},${doubleArray[1]},${doubleArray[2]}")

    val lastNameArray = arrayOf("Chang", "Lee", "Chen")
    var firstNameArray = arrayOf("Yaya", "James", "Chris")

    println("lastNameArray:${lastNameArray[0]},${lastNameArray[1]},${lastNameArray[2]}")
    println("firstNameArray:${firstNameArray[0]},${firstNameArray[1]},${firstNameArray[2]}")
}