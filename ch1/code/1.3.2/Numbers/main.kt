fun main() {
    var timestampe = 1614556800000L //2021年3月1日星期一08:00:00
    var int_number = 1234 //整數1234
    var short_number = 1234 //短整數1234
    var double_number = 1.234 //小數點1.234
    var float_number = 1.234F //長小數點1.234
    var byte_number = 0b00010110 //8進位的22
    var hex_number = 0x16 //16進位的22

    println("timestampe:${timestampe}")
    println("int_number:${int_number}")
    println("short_number:${short_number}")
    println("double_number:${double_number}")
    println("float_number:${float_number}")
    println("byte_number:${byte_number}")
    println("hex_number:${hex_number}")
}
